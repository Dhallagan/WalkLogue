// 32x32 pixel art sprites for WalkBuddy
// '#' = filled pixel, '.' = empty
// Each string is one row, 32 chars wide

function parse(rows: string[]): number[][] {
  return rows.map((row) => [...row].map((c) => (c === "#" ? 1 : 0)));
}

